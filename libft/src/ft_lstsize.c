#include "../inc/libft.h"

int	ft_lstsize(t_list *lst)
{
	size_t	i;
	t_list	*current;

	current = lst;
	i = 0;
	while (current != NULL)
	{
		i++;
		current = current->next;
	}
	return (i);
}
